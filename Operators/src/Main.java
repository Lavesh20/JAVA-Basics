public class Main {
    public static void main(String[] args) {
       //Addition Operators
        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;
        System.out.println("The addition of two numbers are: " + sum);

        //Subtraction
        int sub = num2 - num1;
        System.out.println("The substraction of two numbers are: "+sub);

        //Multiplication
        int multiply = num1*num2;
        System.out.println("Multiplication: "+multiply);

        //Division
        int div = num2/num1;
        System.out.println("Division: "+div);

        //Modulas operator
        int rem = num2%num1;
        System.out.println("Remainder: "+rem);
    }
}